sealed class LoginEvent {
    data class Login(val email: String, val password: String) : LoginEvent()
    object SignUp : LoginEvent()
    object LoginAsGuest : LoginEvent()
    object NavigateBack : LoginEvent()
}

@Preview
@Composable
fun LoginPreview(){
    Login{}
}


@OptIn(ExperimentalMultiplatform::class)
@Composable
fun Login(onNavigationEvent: (LoginEvent) -> Unit) {


    Scaffold(
        topBar = {
            LoginSignUpTopAppBar(topAppBarText = "Sign In",
                { onNavigationEvent(LoginEvent.NavigateBack) })
        },
        content = {
            LoginSignUpScreen(
                onLoggedInAsGuest = { onNavigationEvent(LoginEvent.LoginAsGuest) },
                modifier = Modifier.fillMaxWidth()
            ) {
                LoginContent(onLoginSubmitted = {
                    email, password -> onNavigationEvent(LoginEvent.Login(email, password))
                })
            }
        }
    )
}

@Composable
fun LoginContent(
    onLoginSubmitted: (email: String, password: String) -> Unit
) {
    Column(modifier = Modifier.fillMaxWidth()) {
        val focusRequester = remember { FocusRequester() }
        val emailState = remember { EmailState() }
        Email(emailState, onImeAction = { focusRequester.requestFocus() })

        Spacer(modifier = Modifier.height(16.dp))

        val passwordState = remember { PasswordState() }
        Password(
            label = "Password",
            passwordState = passwordState,
            modifier = Modifier.focusRequester(focusRequester),
            onImeAction = { onLoginSubmitted(emailState.text, passwordState.text) }
        )
        Spacer(modifier = Modifier.height(16.dp))
        Button(
            onClick = { onLoginSubmitted(emailState.text, passwordState.text) },
            modifier = Modifier
                .fillMaxWidth()
                .padding(vertical = 16.dp),
            enabled = emailState.isValid && passwordState.isValid
        ) {
            Text(
                text = "Login"
            )
        }

    }
}


@Composable
fun LoginSignUpScreen(
    onLoggedInAsGuest: () -> Unit,
    modifier: Modifier = Modifier,
    content: @Composable() () -> Unit
) {
    LazyColumn(modifier = modifier) {
        item {
            Spacer(modifier = Modifier.height(44.dp))
            Box(
                modifier = Modifier
                    .fillMaxWidth()
                    .padding(horizontal = 20.dp)
            ) {
                content()
            }
            Spacer(modifier = Modifier.height(16.dp))
            OrLoggedInAsGuest(
                onLoggedInAsGuest = onLoggedInAsGuest,
                modifier = Modifier
                    .fillMaxWidth()
                    .padding(horizontal = 20.dp)
            )
        }
    }
}


